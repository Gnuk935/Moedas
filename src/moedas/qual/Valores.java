package moedas.qual;

public class Valores {
	//Define as variaveis e seus metodos
	
	//Valores interos
	private int n;
	private int r;
	private int a;
	private int nR;
	
	//Valores decimais
	private Double nRc;
	private Double nRd;
	private Double nRv;
	private Double nRcc;
	private Double nRu;
	
	
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getnR() {
		return nR;
	}
	public void setnR(int nR) {
		this.nR = nR;
	}
	public Double getnRc() {
		return nRc;
	}
	public void setnRc(Double nRc) {
		this.nRc = nRc;
	}
	public Double getnRd() {
		return nRd;
	}
	public void setnRd(Double nRd) {
		this.nRd = nRd;
	}
	public Double getnRv() {
		return nRv;
	}
	public void setnRv(Double nRv) {
		this.nRv = nRv;
	}
	public Double getnRcc() {
		return nRcc;
	}
	public void setnRcc(Double nRcc) {
		this.nRcc = nRcc;
	}
	public Double getnRu() {
		return nRu;
	}
	public void setnRu(Double nRu) {
		this.nRu = nRu;
	}
	
}